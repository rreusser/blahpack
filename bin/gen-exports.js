/**
 * Generate the root barrel (index.js) and patch package.json exports map.
 *
 * For each module under lib/{blas,lapack}/base/<routine>/:
 *   - Emits `export { default as <routine> }` in the barrel
 *   - If the module index has a named `ndarray` export, also emits
 *     `export { ndarray as <routine>Ndarray }` in the barrel
 *   - Adds a subpath entry to the exports map
 *
 * Usage:
 *   node bin/gen-exports.js           # write index.js + patch package.json
 *   node bin/gen-exports.js --dry     # print without writing
 */

import { readdirSync, existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join, resolve } from 'node:path';

const ROOT = resolve( import.meta.dirname, '..' );
const DRY = process.argv.includes( '--dry' );

// --- Discover modules -------------------------------------------------------

function discoverModules( pkg ) {
	const base = join( ROOT, 'lib', pkg, 'base' );
	if ( !existsSync( base ) ) return [];
	return readdirSync( base, { withFileTypes: true } )
		.filter( ( e ) => e.isDirectory() )
		.map( ( e ) => ({
			pkg,
			name: e.name,
			indexPath: join( base, e.name, 'lib', 'index.js' ),
			subpath: `./${pkg}/base/${e.name}`,
			relIndex: `./lib/${pkg}/base/${e.name}/lib/index.js`
		}) )
		.filter( ( m ) => existsSync( m.indexPath ) )
		.sort( ( a, b ) => a.name < b.name ? -1 : 1 );
}

const modules = [
	...discoverModules( 'blas' ),
	...discoverModules( 'lapack' )
];

console.log( `Found ${modules.length} modules` );

// --- Determine which modules expose a named ndarray export ------------------

// Modules with `export { ... ndarray ... }` in their index.js have ndarray as
// a proper named export. Modules with `export default main` only expose
// ndarray as an attached property (main.ndarray); those still work for callers
// but don't need a separate barrel line.
function hasNamedNdarrayExport( indexPath ) {
	try {
		const src = readFileSync( indexPath, 'utf8' );
		return /export\s*\{[^}]*\bndarray\b/.test( src );
	} catch {
		return false;
	}
}

// --- Generate barrel index.js -----------------------------------------------

const barrelLines = [
	'// AUTO-GENERATED by bin/gen-exports.js — do not edit by hand',
	''
];

for ( const m of modules ) {
	const hasNdarray = hasNamedNdarrayExport( m.indexPath );
	if ( hasNdarray ) {
		barrelLines.push(
			`export { default as ${m.name}, ndarray as ${m.name}Ndarray } from '${m.relIndex}';`
		);
	} else {
		barrelLines.push( `export { default as ${m.name} } from '${m.relIndex}';` );
	}
}
barrelLines.push( '' );
const barrelSrc = barrelLines.join( '\n' );

// --- Generate exports map ---------------------------------------------------

const exportsMap = { '.': './index.js' };
for ( const m of modules ) {
	exportsMap[ m.subpath ] = m.relIndex;
}

// --- Patch package.json -----------------------------------------------------

const pkgPath = join( ROOT, 'package.json' );
const pkg = JSON.parse( readFileSync( pkgPath, 'utf8' ) );

pkg.name = '@rreusser/blapack';
pkg.main = './index.js';
pkg.exports = exportsMap;
pkg.files = [ 'lib/', 'index.js' ];

// Swap @stdlib/stdlib mega-dep for the 9 actually-used scoped packages.
const STDLIB_VERSION = '^0.3.3';
pkg.dependencies = {
	'@stdlib/array': STDLIB_VERSION,
	'@stdlib/assert': STDLIB_VERSION,
	'@stdlib/blas': STDLIB_VERSION,
	'@stdlib/complex': STDLIB_VERSION,
	'@stdlib/constants': STDLIB_VERSION,
	'@stdlib/math': STDLIB_VERSION,
	'@stdlib/strided': STDLIB_VERSION,
	'@stdlib/string': STDLIB_VERSION,
	'@stdlib/utils': STDLIB_VERSION
};

// Ensure gen-exports is runnable from npm scripts.
if ( pkg.scripts ) pkg.scripts[ 'gen-exports' ] = 'node bin/gen-exports.js';

// Keep key order clean: name, version, description, main, type, exports, files, ...
const KEY_ORDER = [ 'name', 'version', 'description', 'main', 'type', 'exports', 'files', 'scripts', 'keywords', 'author', 'license', 'dependencies', 'devDependencies' ];
const ordered = {};
for ( const k of KEY_ORDER ) if ( k in pkg ) ordered[ k ] = pkg[ k ];
for ( const k of Object.keys( pkg ) ) if ( !( k in ordered ) ) ordered[ k ] = pkg[ k ];

const pkgSrc = JSON.stringify( ordered, null, 2 ) + '\n';

// --- Write or dry-run -------------------------------------------------------

if ( DRY ) {
	console.log( '\n=== index.js (first 20 lines) ===' );
	console.log( barrelLines.slice( 0, 22 ).join( '\n' ) );
	console.log( `... (${modules.length} modules total)` );
	console.log( '\n=== package.json exports map (first 8 entries) ===' );
	const entries = Object.entries( exportsMap ).slice( 0, 8 );
	console.log( JSON.stringify( Object.fromEntries( entries ), null, 2 ) );
	console.log( `... (${Object.keys( exportsMap ).length} entries total)` );
} else {
	writeFileSync( join( ROOT, 'index.js' ), barrelSrc );
	writeFileSync( pkgPath, pkgSrc );
	console.log( `Wrote index.js (${modules.length} modules)` );
	console.log( `Patched package.json (${Object.keys( exportsMap ).length} exports entries)` );
}
